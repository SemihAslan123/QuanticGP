                <b><span class="listeArticleTitre">4.1 Photo et vidéo : quel matériel emporter ?</span></b>

                <br><br>

                <b>A] <i class="fas fa-camera"></i> Le matériel idéal pour les passionnés</b>

                <br><br>

                Si vous possédez un reflex, privilégiez deux objectifs : <b>24-105 mm</b>, idéal pour les photos dans le village F1 ou les stands. 
                <b>70-200 mm ou 70-300 mm</b>, parfait pour capturer des détails ou des voitures en mouvement sur la piste. Cependant, si vous cherchez une 
                solution différente, les GoPro et autres caméras d’action sont idéales pour capturer l’ambiance ou filmer vos déplacements, mais leur grand 
                angle limite leur efficacité pour photographier la piste.

                <br><br>

                <b>B] <i class="fas fa-mobile-alt"></i> Smartphone : pour la simplicité</b>

                <br><br>

                Les smartphones modernes offrent de belles performances, mais évitez de zoomer lors de la prise de vue, car cela réduit la qualité. 
                Prenez des clichés sans zoom, puis recadrez-les avec un logiciel plus tard. N'hésitez pas à installez des applications qui permettent de 
                photographier en RAW ou de régler manuellement des paramètres comme la vitesse d’obturation et l’exposition.

                <br><br>

                <b>C] <i class="fas fa-tools"></i> Accessoires pratiques</b>

                <br><br>

                Le <b>monopod</b> : Léger, compact et adapté aux espaces restreints des tribunes, un monopod est idéal pour stabiliser vos prises de vue. <br>
                <b>Batteries supplémentaires</b> : Une batterie ne suffira pas si vous manipulez fréquemment l’écran ou filmez. Prévoyez-en au moins une ou deux supplémentaires. <br>
                <b>Cartes mémoire</b>: Emportez plusieurs cartes mémoire de grande capacité (au moins 64 Go), surtout si vous photographiez en RAW ou filmez en haute définition (1080p ou 4K).

                <br><br><br>

                <b><span class="listeArticleTitre">4.2 Technique : conseils pour des clichés réussis</span></b>

                <br><br>

                <b>A] <i class="fas fa-dumbbell"></i> Entraînez-vous avant l’événement</b>

                <br><br>

                Testez votre matériel avec des voitures en mouvement près de chez vous. 
                Cela vous permettra d’ajuster vos réglages et de vous familiariser avec les contraintes. 
                Entraînez-vous également à suivre un véhicule en mouvement avec fluidité

                <br><br>

                <b>B] <i class="fas fa-cogs"></i> Réglez votre appareil en mode manuel</b>

                <br><br>

                Pour de meilleurs résultats, réglez votre appareil en mode manuel avec une vitesse d’obturation rapide (1/1000 ou plus) pour figer les voitures en mouvement. 
                Adaptez les ISO selon la lumière (400 par beau temps, 800 sous un ciel couvert) et utilisez le format RAW pour faciliter le post-traitement.

                <br><br>

                <b>C] <i class="fas fa-level-up-alt"></i> Stabilisation</b>

                <br><br>

                Si votre téléobjectif propose plusieurs modes, choisissez la stabilisation horizontale (mode II) pour suivre les voitures en mouvement. 
                Cela permettra de minimiser les vibrations lorsque vous effectuez des panoramiques, garantissant des images nettes tout en capturant le mouvement.
